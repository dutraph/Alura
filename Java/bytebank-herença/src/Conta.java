public class Conta {
	
	private int agencia;
	private int numero;
	private int digito;
	
	public void setAgencia(int agencia) {
		this.agencia = agencia;
	}

	public int getAgencia() {
		return this.agencia;
	}
	
	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	public int getNumero() {
		return this.numero;
	}
	
	public void setDigito(int digito) {
		this.digito = digito;
	}
	
	public int getDigito() {
		return this.digito;
	}
	
}


	